package waffles.utils.sys.input.mouse;

/**
 * The {@code MouseButton} enum defines all buttons available on mouse input.
 *
 * @author Waffles
 * @since Jun 25, 2015
 * @version 1.0
 */
public enum MouseButton
{
	/**
	 * The middle mouse button.
	 */
	MIDDLE,
	/**
	 * The right mouse button.
	 */
	RIGHT,
	/**
	 * The left mouse button.
	 */
	LEFT,
	/**
	 * An undefined button.
	 */
	NONE;
}